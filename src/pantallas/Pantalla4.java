    void agregar(){
        dtm.addRow(new Object[] {
            txtId.getText(),txtProducto.getText(),txtCantidad.getText()
        });
        
    }
    void eliminar(){
        int fila=tblDatos.getSelectedRow();
        dtm.removeRow(fila);
    }
    void actualizar(){
        int fila=tblDatos.getSelectedRow();
        dtm.setValueAt(txtId.getText(), fila, 0);
        dtm.setValueAt(txtProducto.getText(), fila, 1);
        dtm.setValueAt(txtCantidad.getText(), fila, 2);
    }
    void limpiarTabla(){
        int filas= dtm.getRowCount();
                for(int i=0;i<filas;i++){
                  dtm.removeRow(0);
        }
    }
